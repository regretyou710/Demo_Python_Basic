# 賦值運算符
# = 可以將等號右側的值賦值給等號左側的變量
# +=  a += 5 相當於 a = a + 5 
# -=  a -= 5 相當於 a = a - 5 
# *=  a *= 5 相當於 a = a * 5 
# **= a **= 5 相當於 a = a ** 5 
# /=  a /= 5 相當於 a = a / 5 
# //= a //= 5 相當於 a = a // 5 
# %=  a %= 5 相當於 a = a % 5 
a = 10
# a = a + 5
# a += 5
a -= 5
a *= 5
a **= 2
a /= 25
a = 25.0 # 在對浮點數做算術運算時，結果也會返回一個浮點數
a //= 5
a = 5
a %= 4

print('a =',a)