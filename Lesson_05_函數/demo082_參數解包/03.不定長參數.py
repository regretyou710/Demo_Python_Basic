# 傳遞實參時，也可以在序列類型的參數前添加星號，這樣他會自動將序列中的元素依次作為參數傳遞
# 這裡要求序列中元素的個數必須和形參的個數的一致
# fn4(*t)    

# 創建一個字典
d = {'a':100,'b':200,'c':300}
# 通過 **來對一個字典進行解包操作
fn4(**d)